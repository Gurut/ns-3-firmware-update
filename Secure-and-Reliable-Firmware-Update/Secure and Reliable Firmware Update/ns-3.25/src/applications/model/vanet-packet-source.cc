TypeId
VanetPacketSource::GetTypeId (void)
{
  static TypeId tid = TypeId ("ns3::VanetPacketSource")
    .SetParent<Application> ()
    .SetGroupName("Applications")
    .AddConstructor<VanetPacketSource> ()
    .AddAttribute ("MaxPackets", 
                   "The maximum number of packets the application will send",
                   UintegerValue (100),
                   MakeUintegerAccessor (&VanetPacketSource::m_count),
                   MakeUintegerChecker<uint32_t> ())
    .AddAttribute ("Interval", 
                   "The time to wait between packets",
                   TimeValue (Seconds (1.0)),
                   MakeTimeAccessor (&VanetPacketSource::m_interval),
                   MakeTimeChecker ())
    .AddAttribute ("ConnectionReqPeriod",
                   "Connection Request Period",
                   TimeValue (Seconds (2.0)),
                   MakeTimeAccessor (&VanetPacketSource::m_connReqPeriod),
                   MakeTimeChecker ())
    .AddAttribute ("RemoteAddress", 
                   "The destination Address of the outbound packets",
                   AddressValue (),
                   MakeAddressAccessor (&VanetPacketSource::m_peerAddress),
                   MakeAddressChecker ())
    .AddAttribute ("RemotePort", 
                   "The destination port of the outbound packets",
                   UintegerValue (0),
                   MakeUintegerAccessor (&VanetPacketSource::m_peerPort),
                   MakeUintegerChecker<uint16_t> ())
    .AddAttribute ("PacketSize", "Size of echo data in outbound packets",
                   UintegerValue (100),
                   MakeUintegerAccessor (&VanetPacketSource::SetDataSize,
                                         &VanetPacketSource::GetDataSize),
                   MakeUintegerChecker<uint32_t> ())
    .AddTraceSource ("Tx", "A new packet is created and is sent",
                     MakeTraceSourceAccessor (&VanetPacketSource::m_txTrace),
                     "ns3::Packet::TracedCallback")
  ;
  return tid;
}

VanetPacketSource::VanetPacketSource ()
{
  NS_LOG_FUNCTION (this);
  m_sent = 0;
  m_socket = 0;
  m_sendEvent = EventId ();
  m_data = 0;
  m_dataSize = 0;
  m_tSent = 0;
  m_tReceived = 0;
  m_connected = false;
}

VanetPacketSource::~VanetPacketSource()
{
  NS_LOG_FUNCTION (this);
  m_socket = 0;

  delete [] m_data;
  m_data = 0;
  m_dataSize = 0;
}

